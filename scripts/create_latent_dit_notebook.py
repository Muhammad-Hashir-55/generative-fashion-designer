    "# Initialize Latent DiT Model\n",
    "# 64x64 image -> VAE factor 8 -> 8x8 latent grid\n",
    "dit = LatentDiT(in_channels=4, embed_dim=256, depth=6, n_heads=8, latent_size=8).to(DEVICE)\n",
    "model = StableDDPM(dit, timesteps=1000).to(DEVICE)\n",
    "\n",
    "optimizer = torch.optim.AdamW(model.parameters(), lr=LR, weight_decay=1e-4)\n",
    "scaler = torch.cuda.amp.GradScaler(enabled=True)\n",
    "\n",
    "print(f\"Starting Latent DiT training on {DEVICE}...\")\n",
    "\n",
    "for epoch in range(1, EPOCHS + 1):\n",
    "    model.train()\n",
    "    total_loss = 0.0\n",
    "    valid_batches = 0\n",
    "    \n",
    "    pbar = tqdm(dataloader, desc=f\"Epoch {epoch}/{EPOCHS}\")\n",
    "    for batch in pbar:\n",
    "        images, _ = batch\n",
    "        images = images.to(DEVICE)\n",
    "        \n",
    "        optimizer.zero_grad(set_to_none=True)\n",
    "        \n",
    "        # -- LATENT COMPRESSION STEP --\n",
    "        with torch.no_grad():\n",
    "            # Compress raw 64x64x3 pixel images into 8x8x4 latent representations\n",
    "            latents = vae.encode(images).latent_dist.sample()\n",
    "            latents = latents * VAE_SCALE_FACTOR\n",
    "            \n",
    "        # -- DIFFUSION TRANSFORMER STEP --\n",
    "        with torch.cuda.amp.autocast(enabled=True):\n",
    "            loss = model(latents)\n",
    "            \n",
    "        # Safe NaN guard\n",
    "        if not math.isfinite(loss.item()):\n",
    "            print(f\"\\nWARNING: NaN loss detected at epoch {epoch}. Skipping batch.\")\n",
    "            continue\n",
    "            \n",
    "        scaler.scale(loss).backward()\n",
    "        \n",
    "        # Strict Gradient Clipping\n",
    "        scaler.unscale_(optimizer)\n",
    "        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)\n",
    "        \n",
    "        scaler.step(optimizer)\n",
    "        scaler.update()\n",
    "        \n",
    "        total_loss += loss.item()\n",
    "        valid_batches += 1\n",
    "        pbar.set_postfix({\"loss\": f\"{loss.item():.4f}\"})\n",
    "        \n",
    "    avg_loss = total_loss / max(1, valid_batches)\n",
    "    print(f\"Epoch {epoch} | Average Loss: {avg_loss:.4f}\")\n",
    "    \n",
    "    torch.save({\n",
    "        \"epoch\": epoch,\n",
    "        \"model_state_dict\": model.state_dict(),\n",
    "        \"optimizer_state_dict\": optimizer.state_dict(),\n",
    "        \"loss\": avg_loss\n",
    "    }, os.path.join(OUTPUT_DIR, \"latent_dit_latest.pt\"))\n",
    "    \n",
    "    if epoch % 5 == 0 or epoch == 1:\n",
    "        model.eval()\n",
    "        print(f\"Generating Latent DiT samples for epoch {epoch}...\")\n",
    "        with torch.no_grad():\n",
    "            # We pass the frozen VAE into the sample function so it can decode the latents back to pixels\n",
    "            samples = model.sample(num_samples=16, vae=vae, latent_size=8, channels=4, device=DEVICE)\n",
    "            grid = vutils.make_grid(samples, nrow=4, padding=2, normalize=False)\n",
    "            \n",
    "            sample_path = os.path.join(OUTPUT_DIR, f\"latent_dit_epoch_{epoch:03d}.png\")\n",
    "            vutils.save_image(samples, sample_path, nrow=4, padding=2, normalize=False)\n",
    "            \n",
    "            plt.figure(figsize=(8, 8))\n",
    "            plt.axis(\"off\")\n",
    "            plt.title(f\"Latent DiT Epoch {epoch} Samples\")\n",
    "            plt.imshow(np.transpose(grid.cpu().numpy(), (1, 2, 0)))\n",
    "            plt.show()\n",
    "\n",
    "print(\"Latent DiT Training Complete!\")\n"
