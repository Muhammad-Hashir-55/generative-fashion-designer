    "# 2. Diffusion Transformer (DiT) Architecture\n",
    "class SinusoidalPositionEmbeddings(nn.Module):\n",
    "    def __init__(self, dim: int):\n",
    "        super().__init__()\n",
    "        self.dim = dim\n",
    "\n",
    "    def forward(self, time: torch.Tensor):\n",
    "        device = time.device\n",
    "        half_dim = self.dim // 2\n",
    "        embeddings = math.log(10000) / (half_dim - 1)\n",
    "        embeddings = torch.exp(torch.arange(half_dim, device=device) * -embeddings)\n",
    "        embeddings = time[:, None] * embeddings[None, :]\n",
    "        embeddings = torch.cat((embeddings.sin(), embeddings.cos()), dim=-1)\n",
    "        return embeddings\n",
    "\n",
    "class AdaLN(nn.Module):\n",
    "    \"\"\"Adaptive Layer Normalization (injects time-step info into Transformer blocks)\"\"\"\n",
    "    def __init__(self, dim: int):\n",
    "        super().__init__()\n",
    "        self.norm = nn.LayerNorm(dim, elementwise_affine=False)\n",
    "        self.linear = nn.Linear(dim, 2 * dim)\n",
    "        \n",
    "    def forward(self, x, c):\n",
    "        shift, scale = self.linear(c).chunk(2, dim=-1)\n",
    "        return self.norm(x) * (1 + scale.unsqueeze(1)) + shift.unsqueeze(1)\n",
    "\n",
    "class DiTBlock(nn.Module):\n",
    "    def __init__(self, dim: int, n_heads: int):\n",
    "        super().__init__()\n",
    "        self.norm1 = AdaLN(dim)\n",
    "        self.attn = nn.MultiheadAttention(dim, n_heads, batch_first=True)\n",
    "        self.norm2 = AdaLN(dim)\n",
    "        self.mlp = nn.Sequential(\n",
    "            nn.Linear(dim, dim * 4),\n",
    "            nn.GELU(),\n",
    "            nn.Linear(dim * 4, dim)\n",
    "        )\n",
    "        \n",
    "    def forward(self, x, c):\n",
    "        nx = self.norm1(x, c)\n",
    "        x = x + self.attn(nx, nx, nx)[0]\n",
    "        nx = self.norm2(x, c)\n",
    "        x = x + self.mlp(nx)\n",
    "        return x\n",
    "\n",
    "class LatentDiT(nn.Module):\n",
    "    \"\"\"Diffusion Transformer operating on the 8x8 compressed latent space.\"\"\"\n",
    "    def __init__(self, in_channels=4, embed_dim=256, depth=6, n_heads=8, latent_size=8):\n",
    "        super().__init__()\n",
    "        # Linear patch embedding (treats each 1x1 latent pixel as a token)\n",
    "        self.patch_embed = nn.Conv2d(in_channels, embed_dim, kernel_size=1)\n",
    "        self.pos_embed = nn.Parameter(torch.zeros(1, latent_size * latent_size, embed_dim))\n",
    "        \n",
    "        # Time embedding MLP\n",
    "        self.time_mlp = nn.Sequential(\n",
    "            SinusoidalPositionEmbeddings(embed_dim),\n",
    "            nn.Linear(embed_dim, embed_dim),\n",
    "            nn.GELU(),\n",
    "            nn.Linear(embed_dim, embed_dim)\n",
    "        )\n",
    "        \n",
    "        self.blocks = nn.ModuleList([DiTBlock(embed_dim, n_heads) for _ in range(depth)])\n",
    "        self.norm_out = AdaLN(embed_dim)\n",
    "        self.head = nn.Linear(embed_dim, in_channels)\n",
    "\n",
    "    def forward(self, x, time):\n",
    "        B, C, H, W = x.shape\n",
    "        \n",
    "        # Flatten 8x8 grid into sequence of 64 tokens\n",
    "        x = self.patch_embed(x).flatten(2).transpose(1, 2) # [B, 64, D]\n",
    "        x = x + self.pos_embed\n",
    "        \n",
    "        c = self.time_mlp(time)\n",
    "        for block in self.blocks:\n",
    "            x = block(x, c)\n",
    "            \n",
    "        x = self.norm_out(x, c)\n",
    "        x = self.head(x) # [B, 64, 4]\n",
    "        \n",
    "        # Reshape back to 8x8 grid\n",
    "        x = x.transpose(1, 2).reshape(B, C, H, W)\n",
    "        return x\n",
    "\n",
    "class StableDDPM(nn.Module):\n",
    "    def __init__(self, model: LatentDiT, timesteps: int = 1000):\n",
    "        super().__init__()\n",
    "        self.model = model\n",
    "        self.timesteps = timesteps\n",
    "\n",
    "        beta = torch.linspace(0.0001, 0.02, timesteps)\n",
    "        alpha = 1. - beta\n",
    "        alpha_hat = torch.cumprod(alpha, dim=0)\n",
    "\n",
    "        self.register_buffer(\"beta\", beta)\n",
    "        self.register_buffer(\"alpha\", alpha)\n",
    "        self.register_buffer(\"alpha_hat\", alpha_hat)\n",
    "\n",
    "    def forward(self, x):\n",
    "        device = x.device\n",
    "        t = torch.randint(0, self.timesteps, (x.shape[0],), device=device).long()\n",
    "        noise = torch.randn_like(x)\n",
    "\n",
    "        alpha_hat_t = self.alpha_hat[t].view(-1, 1, 1, 1)\n",
    "        \n",
    "        # CRITICAL FIX for NaN issues\n",
    "        alpha_hat_t = torch.clamp(alpha_hat_t, max=0.9999)\n",
    "        one_minus_alpha_hat_t = torch.clamp(1.0 - alpha_hat_t, min=1e-5)\n",
    "        \n",
    "        x_noisy = torch.sqrt(alpha_hat_t) * x + torch.sqrt(one_minus_alpha_hat_t) * noise\n",
    "        pred_noise = self.model(x_noisy, t)\n",
    "        return F.mse_loss(pred_noise, noise)\n",
    "\n",
    "    @torch.no_grad()\n",
    "    def sample(self, num_samples, vae, latent_size=8, channels=4, device=\"cuda\"):\n",
    "        \"\"\"Generates samples by running diffusion in latent space, then decoding via VAE.\"\"\"\n",
    "        # 1. Start with pure noise in LATENT space (8x8x4)\n",
    "        x = torch.randn((num_samples, channels, latent_size, latent_size), device=device)\n",
    "        \n",
    "        for i in tqdm(reversed(range(self.timesteps)), desc=\"Latent Sampling\", leave=False, total=self.timesteps):\n",
    "            t = torch.full((num_samples,), i, device=device, dtype=torch.long)\n",
    "            pred_noise = self.model(x, t)\n",
    "            \n",
    "            alpha_t = self.alpha[t].view(-1, 1, 1, 1)\n",
    "            alpha_hat_t = self.alpha_hat[t].view(-1, 1, 1, 1)\n",
    "            beta_t = self.beta[t].view(-1, 1, 1, 1)\n",
    "            \n",
    "            one_minus_alpha_hat_t = torch.clamp(1.0 - alpha_hat_t, min=1e-5)\n",
    "\n",
    "            noise = torch.randn_like(x) if i > 0 else torch.zeros_like(x)\n",
    "            x = (1 / torch.sqrt(alpha_t)) * (x - ((1 - alpha_t) / torch.sqrt(one_minus_alpha_hat_t)) * pred_noise) + torch.sqrt(beta_t) * noise\n",
    "\n",
    "        # 2. Decode the latents back into 64x64 pixel images using the VAE\n",
    "        x = x / VAE_SCALE_FACTOR\n",
    "        decoded_images = vae.decode(x).sample\n",
    "        \n",
    "        # 3. Denormalize pixel images from [-1, 1] to [0, 1]\n",
    "        decoded_images = (decoded_images.clamp(-1, 1) + 1) / 2\n",
    "        return decoded_images\n"
